async function main() {
  console.log(`\n🏥 MedScribe Clinic Seeder`);
  console.log(`════════════════════════════════════════════════\n`);
  console.log(`Total clinics to seed: ${clinics.length}`);

  // 1. Create table
  console.log('\n📋 Step 1: Creating clinics table...');
  await createTable();

  // Wait for schema cache to reload
  if (DB_PASSWORD) {
    console.log('\n⏳ Waiting for schema cache reload...');
    await new Promise(r => setTimeout(r, 3000));
  }

  // 2. Clear existing data
  console.log('\n🗑️  Step 2: Clearing existing clinic data...');
  const deleteRes = await fetch(`${SUPABASE_URL}/rest/v1/clinics?id=not.is.null`, {
    method: 'DELETE',
    headers: {
      'apikey': SUPABASE_KEY,
      'Authorization': `Bearer ${SUPABASE_KEY}`,
      'Content-Type': 'application/json',
      'Prefer': 'return=minimal',
    },
  });

  if (deleteRes.ok) {
    console.log('   ✅ Existing data cleared');
  } else {
    const errText = await deleteRes.text();
    console.log(`   ⚠️  Delete response: ${deleteRes.status} - ${errText}`);
  }

  // 3. Insert clinics in batches
  console.log('\n📥 Step 3: Inserting clinic data...');

  const BATCH_SIZE = 25;
  let inserted = 0;
  let failed = 0;

  for (let i = 0; i < clinics.length; i += BATCH_SIZE) {
    const batch = clinics.slice(i, i + BATCH_SIZE);
    const batchNum = Math.floor(i / BATCH_SIZE) + 1;
    const totalBatches = Math.ceil(clinics.length / BATCH_SIZE);

    const res = await fetch(`${SUPABASE_URL}/rest/v1/clinics`, {
      method: 'POST',
      headers: {
        'apikey': SUPABASE_KEY,
        'Authorization': `Bearer ${SUPABASE_KEY}`,
        'Content-Type': 'application/json',
        'Prefer': 'return=representation',
      },
      body: JSON.stringify(batch),
    });

    if (res.ok) {
      const data = await res.json();
      inserted += data.length;
      console.log(`   Batch ${batchNum}/${totalBatches}: ✅ ${data.length} clinics inserted`);
    } else {
      const errText = await res.text();
      failed += batch.length;
      console.error(`   Batch ${batchNum}/${totalBatches}: ❌ Failed - ${res.status}: ${errText}`);
    }
  }

  // 4. Summary
  console.log('\n════════════════════════════════════════════════');
  console.log('📊 Summary:');

  // Count by country
  const countryCounts = {};
  const typeCounts = { hospital: 0, clinic: 0, practice: 0 };
  const specialtyCounts = {};

  clinics.forEach(c => {
    countryCounts[c.country] = (countryCounts[c.country] || 0) + 1;
    typeCounts[c.type] = (typeCounts[c.type] || 0) + 1;
    c.specialty.forEach(s => {
      specialtyCounts[s] = (specialtyCounts[s] || 0) + 1;
    });
  });

  console.log(`\n   ✅ Inserted: ${inserted}`);
  console.log(`   ❌ Failed: ${failed}`);
  console.log(`\n   📍 By Country:`);
  Object.entries(countryCounts).sort((a, b) => b[1] - a[1]).forEach(([country, count]) => {
    console.log(`      ${country}: ${count}`);
  });
  console.log(`\n   🏢 By Type:`);
  Object.entries(typeCounts).forEach(([type, count]) => {
    console.log(`      ${type}: ${count}`);
  });
  console.log(`\n   🏥 Top Specialties:`);
  Object.entries(specialtyCounts).sort((a, b) => b[1] - a[1]).slice(0, 15).forEach(([spec, count]) => {
    console.log(`      ${spec}: ${count}`);
  });

  console.log('\n════════════════════════════════════════════════\n');
}

main().catch(err => {
  console.error('Fatal error:', err);
  process.exit(1);
});